    fun envFunction(offset: Long, ptrSize: Int = 8): String? = lookup(FUNCTIONS, offset, ptrSize)

    fun vmFunction(offset: Long, ptrSize: Int = 8): String? = lookup(INVOKE, offset, ptrSize)

    fun getEnvOffset(ptrSize: Int = 8): Long = INVOKE.indexOf("GetEnv").toLong() * ptrSize

    private fun lookup(table: Array<String>, offset: Long, ptrSize: Int): String? {
        if (offset < 0 || offset % ptrSize != 0L) return null
        val i = (offset / ptrSize).toInt()
        if (i !in table.indices) return null
        val n = table[i]
        return if (n.startsWith("reserved")) null else n
    }
